    private void loadComboBox() throws SQLException {
        jComboBoxDanToc.removeAllItems();
        jComboBoxLop.removeAllItems();
        jComboBoxTonGiao.removeAllItems();

        List<DanToc> danTocs = danTocCtrl.getAllDanToc();
        for (int i = 0; i<danTocs.size(); i++){
            String str = danTocs.get(i).getId()+"-"+danTocs.get(i).getTenDanToc();
            jComboBoxDanToc.addItem(str);
        }

        List<TonGiao> tonGiaos = tonGiaoCtrl.getAllTonGiao();
        for (int i = 0; i<tonGiaos.size(); i++){
            String str = tonGiaos.get(i).getId()+"-"+tonGiaos.get(i).getTenTonGiao();
            jComboBoxTonGiao.addItem(str);
        }

        List<Lop> lops = lopCtrl.getAllLop();
        for (int i = 0; i<lops.size(); i++){
            String str = lops.get(i).getId()+"-"+lops.get(i).getTenLop();
            jComboBoxLop.addItem(str);
        }
    }

    private void loadDanhSachHoSoHocSinh() throws SQLException {
        if (danhSachHoSoHocSinh.getRowCount()>0) danhSachHoSoHocSinh.setRowCount(0);
        List<HoSoHocSinh> hoSoHocSinhs = hoSoHocSinhCtrl.getAllHoSoHocSinh();
        for (int i = 0; i<hoSoHocSinhs.size(); i++) {
            int id = hoSoHocSinhs.get(i).getId();
            String hoTen = hoSoHocSinhs.get(i).getHoTen();
            String gioiTinh = hoSoHocSinhs.get(i).getGioiTinh();
            String ngaySinh = hoSoHocSinhs.get(i).getNgaySinh();
            String noiSinh = hoSoHocSinhs.get(i).getNoiSinh();
            String lop = lopCtrl.getLopById(hoSoHocSinhs.get(i).getLop_Id()).getTenLop();

            Object[] row = {id, hoTen, gioiTinh, ngaySinh, noiSinh, lop};
            danhSachHoSoHocSinh.addRow(row);
        }
    }
    private boolean checkJtext(javax.swing.JTextField jText) {
        if (jText.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Hãy nhập đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private boolean checkNamSinh(javax.swing.JTextField jText) {
        if (!jText.getText().matches("-?\\d+(\\.\\d+)?"))
        {
            JOptionPane.showMessageDialog(null, "Năm sinh phải là số", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
