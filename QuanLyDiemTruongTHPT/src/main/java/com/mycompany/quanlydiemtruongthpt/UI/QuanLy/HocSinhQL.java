    private void jButtonThemHocSinhActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButtonThemHocSinhActionPerformed
        if (!checkJtext(jTextFieldHoVaTenHS)) return;
        newHoTen = jTextFieldHoVaTenHS.getText();

        if (jRadioButtonNam.isSelected()) newGioiTinh = "Nam";
        if (jRadioButtonNu.isSelected()) newGioiTinh = "Nữ";

        Date date = jDateChooserNgaySinh.getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        newNgaySinh = dateFormat.format(date);

        if (!checkJtext(jTextFieldNoiSinh)) return;
        newNoiSinh = jTextFieldNoiSinh.getText();

        String str = "";
        String[] parts = null;

        str = (String) jComboBoxDanToc.getSelectedItem();
        parts = str.split("-");
        newDanToc_Id = Integer.parseInt(parts[0].trim());

        str = (String) jComboBoxTonGiao.getSelectedItem();
        parts = str.split("-");
        newTonGiao_Id = Integer.parseInt(parts[0].trim());

        if (!checkJtext(jTextFieldHoTenCha)) return;
        newHoTenCha = jTextFieldHoTenCha.getText();


        if (!checkNamSinh(jTextFieldNamSinhCha)) return;
        newNamSinhCha = Integer.valueOf(jTextFieldNamSinhCha.getText());

        if (!checkJtext(jTextFieldNgheNghiep)) return;
        newNgheNghiepCha = jTextFieldNgheNghiep.getText();

        if (!checkJtext(jTextFieldSdtCha)) return;
        newSoDienThoaiCha = jTextFieldSdtCha.getText();

        if (!checkJtext(jTextFieldHoTenMe)) return;
        newHoTenMe = jTextFieldHoTenMe.getText();
        if (!checkNamSinh(jTextFieldNamSinhMe)) return;
        newNamSinhMe = Integer.valueOf(jTextFieldNamSinhMe.getText());
        if (!checkJtext(jTextFieldNgheNghiepMe)) return;
        newNgheNghiepMe = jTextFieldNgheNghiepMe.getText();
        if (!checkJtext(jTextFieldSdtMe)) return;
        newSoDienThoaiMe = jTextFieldSdtMe.getText();

        str = (String) jComboBoxLop.getSelectedItem();
        parts = str.split("-");
        newLop_Id = Integer.parseInt(parts[0].trim());

        newhoSoHocSinh.setHoTen(newHoTen);
        newhoSoHocSinh.setGioiTinh(newGioiTinh);
        newhoSoHocSinh.setNgaySinh(newNgaySinh);
        newhoSoHocSinh.setNoiSinh(newNoiSinh);
        newhoSoHocSinh.setDanToc_Id(newDanToc_Id);
        newhoSoHocSinh.setTonGiao_Id(newTonGiao_Id);
        newhoSoHocSinh.setHoTenCha(newHoTenCha);
        newhoSoHocSinh.setNamSinhCha(newNamSinhCha);
        newhoSoHocSinh.setNgheNghiepCha(newNgheNghiepCha);
        newhoSoHocSinh.setSoDienThoaiCha(newSoDienThoaiCha);
        newhoSoHocSinh.setHoTenMe(newHoTenMe);
        newhoSoHocSinh.setNamSinhMe(newNamSinhMe);
        newhoSoHocSinh.setNgheNghiepMe(newNgheNghiepMe);
        newhoSoHocSinh.setSoDienThoaiMe(newSoDienThoaiMe);
        newhoSoHocSinh.setLop_Id(newLop_Id);

        hoSoHocSinhCtrl.createHoSoHocSinh(newhoSoHocSinh);
        loadDanhSachHoSoHocSinh();
    }//GEN-LAST:event_jButtonThemHocSinhActionPerformed

