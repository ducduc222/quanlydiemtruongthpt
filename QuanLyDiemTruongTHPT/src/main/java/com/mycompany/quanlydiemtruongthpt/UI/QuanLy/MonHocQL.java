    private void loadDanhSachMonHoc() throws SQLException {
        List<MonHoc> list = monHocCtrl.getAllMonHoc();
        if (danhSachMonHoc.getRowCount()>0) danhSachMonHoc.setRowCount(0);
        for (int i = 0; i< list.size(); i++) {
            int Id = list.get(i).getId();
            String tenMonHoc = list.get(i).getTenMonHoc();
            Integer soTiet = list.get(i).getSoTiet();
            Float heSo = list.get(i).getHeSo();
            Object[] row = {Id, tenMonHoc, soTiet, heSo};
            danhSachMonHoc.addRow(row);
        }
    }
