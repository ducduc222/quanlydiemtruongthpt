    private void jTableDanhSachMonHocMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDanhSachMonHocMouseClicked
        int x = jTableDanhSachMonHoc.getSelectedRow();
        if (x>=0) {
            idMonHocDelete = (int) jTableDanhSachMonHoc.getValueAt(x, 0);
            suaTenMonHoc = (String) jTableDanhSachMonHoc.getValueAt(x, 1);
            suaSoTiet = (Integer) jTableDanhSachMonHoc.getValueAt(x, 2);
            suaHeSo = (Float) jTableDanhSachMonHoc.getValueAt(x, 3);

            suaMonHoc.setId(idMonHocDelete);
            suaMonHoc.setTenMonHoc(suaTenMonHoc);
            suaMonHoc.setHeSo(suaHeSo);
            suaMonHoc.setSoTiet(suaSoTiet);
        }
    }//GEN-LAST:event_jTableDanhSachMonHocMouseClicked
