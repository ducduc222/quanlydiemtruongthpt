    public static boolean isDecimalNumber(String input) {
        try {
            // Sử dụng phương thức parseDouble để kiểm tra xem có thể chuyển đổi thành số thập phân hay không
            Double.parseDouble(input);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }
