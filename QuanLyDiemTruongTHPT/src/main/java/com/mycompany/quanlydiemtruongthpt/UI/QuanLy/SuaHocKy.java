    private void jButtonLuuActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_jButtonLuuActionPerformed
        String tenHocKy = jTextFieldTenHocKy.getText();
        Float heSo = Float.valueOf(jTextFieldHeSo.getText());

        String strNamHoc = (String) jComboBoxNamHoc.getSelectedItem();
        String[] parts = strNamHoc.split("-");
        int idNamHoc  = Integer.parseInt(parts[0].trim());

        Boolean hockyhientai = false;

        if (jCheckBoxHocKyHienTai.isSelected()) hockyhientai = true;

        HocKy hocKy = new HocKy();
        hocKy.setId(idHocKy);
        hocKy.setTenHocKy(tenHocKy);
        hocKy.setNamHoc_Id(idNamHoc);
        hocKy.setHocKyHienTai(hockyhientai);
        hocKy.setHeSo(heSo);

        new HocKyCtrl().updateHocKy(hocKy);
        dispose();
    }//GEN-LAST:event_jButtonLuuActionPerformed

