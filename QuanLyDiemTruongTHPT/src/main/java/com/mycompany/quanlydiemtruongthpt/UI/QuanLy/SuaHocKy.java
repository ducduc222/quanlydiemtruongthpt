    public SuaHocKy(int idHocKy) throws SQLException {
        this.idHocKy = idHocKy;
        initComponents();
        HocKy hocKy = new HocKyCtrl().getHocKyById(idHocKy);

        jTextFieldTenHocKy.setText(hocKy.getTenHocKy());
        jTextFieldHeSo.setText(String.valueOf(hocKy.getHeSo()));

        List<NamHoc> namHocs = new NamHocCtrl().getAllNamHoc();
        jComboBoxNamHoc.removeAllItems();
        for (int i = 0; i<namHocs.size(); i++) {
            jComboBoxNamHoc.addItem(namHocs.get(i).getId()+"-"+namHocs.get(i).getTenNamHoc());
            if (namHocs.get(i).getId() == hocKy.getNamHoc_Id()) jComboBoxNamHoc.setSelectedItem(namHocs.get(i).getId()+"-"+namHocs.get(i).getTenNamHoc());
        }

        if (hocKy.getHocKyHienTai()) jCheckBoxHocKyHienTai.setSelected(true);
        setLocationRelativeTo(null);
    }

